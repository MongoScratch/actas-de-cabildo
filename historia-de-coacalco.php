              <div class="section-title">
                <h4 class="text-uppercase">Historia de Coacalco</h4>
              </div>
              <div class="info-branding">
                <p class="text-justify"> La historia de nuestro municipio se remonta hacia miles de años atrás, 
                    hasta la época prehistórica, cuando era paso de mamuts y primitivos pobladores nómadas-recolectores, 
                    que hicieron de este su hogar, dejando huellas de su existir y cohabitar, en un medio donde el frío 
                    de las últimas glaciaciones de hace 14,000 años era la constante.  </p>
<br>
              <dl class="dl-horizontal">
    <br>                <dt><h3> <strong>	Primitivos pobladores cazadores de mamuts </strong></h3> </dt>
        <br>            <dd class="text-justify"> Huellas de estos enormes mamíferos se localizaron en la década de los ochenta del siglo XX 
                        en algunas partes de Coacalco, tal como dieron noticas algunos diarios de circulación nacional, 
                        lo que causó gran expectativa entre la comunidad científica nacional e internacional. En el año 
                        2019, otro hallazgo de restos óseos de catorce mamuts, fueron localizados en el municipio vecino 
                        de Tultitlán, los cuales presentaban características únicas en su tipo a nivel mundial, por lo que
                        este paso de mamuts durante la prehistoria, se ha convertido en un lugar excepcional, que en países 
                        como Rusia y su región siberiana, así como en Austria o Sudáfrica, simplemente no existen, aun siendo 
                        hábitat de mamuts.    </dd>
                        <dd class="text-justify"> Recordemos que los procesos de la evolución de la vida son lentos y se miden por millones de años, 
                        por lo que luego de varios siglos, en la región que hoy conocemos como Mesoamérica, florecieron 
                        culturas imprescindibles en el devenir histórico, marcando nuestras raíces culturales, étnicas, 
                        antropológicas, sentando las bases de parte del bagaje cultural que nos identifica. Toltecas, 
                        teotihuacanos y mexicas encontraron aquí también su hogar, dando testimonio de lo mismo diferentes 
                        restos arqueológicos encontrados en la zona, por ejemplo, en el Conjunto San Francisco, en la Cabecera 
                        Municipal, donde se desenterraron diversos tipos de cerámica de la época tolteca, presumiéndose que la 
                        parroquia de San Francisco está sobre un basamento piramidal; o el hallazgo de restos de una aldea 
                        teotihuacana a la entrada de lo que posteriormente sería parte del ramal del Camino Real de Tierra 
                        Adentro que atravesaba nuestro municipio.  </dd>
                        <dd class="text-justify"> No resulta extraño entonces que estas ricas y ancestrales culturas mantuvieran una simbiosis natural 
                        con el medio ambiente en donde cohabitaban, una constante en todos los pueblos mesoamericanos. El gran 
                        lago del Valle de México, que en realidad eran cinco lagos, algunos de agua dulce o salada, eran hábitat 
                        de muchas especies vegetales y animales, varias endémicas por cierto, mismas que se utilizaban en la 
                        cotidianeidad alimentaria de los pueblos de sus riveras.  </dd>
                        <dd class="text-justify"> En el caso de la parte de Coacalco, el cercano lago de Xaltocan era el asiento natural de aves de 
                        migración como patos, rico alimento incluido en la dieta de los pobladores, así como varios tipos de 
                        peces, anfibios y ajolotes, además de plantas utilizadas en herbolaria tradicional. Aún entrado el 
                        siglo XX, varios lugareños de pueblos vecinos como San Lorenzo o la Magdalena, narran que llevaron 
                        dietas basadas en estos ricos manjares lacustres, y también de alimentos recolectados en las laderas 
                        de la Sierra de Guadalupe.  </dd>
             <br>           <dt> <h3><strong> Ceremonia del Fuego Nuevo </strong> </h3> </dt>
                 <br>       <dd class="text-justify"> La ceremonia prehispánica del fuego nuevo se llevaba a cabo en el imponente Cuautépetl, relatos 
                        rescatados bajo la pluma horrorizada del fray Bernardino de Sahagún, quién trató de entender la 
                        cosmovisión mexica, aun cuando su propio bagaje cultural se contraponía a la realidad mesoamericana. 
                        Y precisamente esta idea del mundo, impactó a los españoles a su llegada este nuevo orbe. La conquista 
                        significará un trago amargo para nuestros pueblos originarios, quienes tuvieron drásticamente que modificar 
                        su entendimiento de la realidad y acoplarse a un nuevo orden de ideas, incomprensibles en muchos casos para 
                        su propia concepción de las cosas.    </dd>
            <br>            <dt> <h3> <strong>	Códices y Coacalco </strong> </h3></dt>
                <br>        <dd class="text-justify"> Es importante mencionar que el registro más antiguo del nombre de Coacalco se menciona en los Anales 
                        de Cuautitilán en referencia al señorío prehispánico al que pertenecían, o Códice Chimalpopoca, relaciones
                         cronológicas históricas anónimas escritas en lengua náhuatl con caracteres latinos,  transcritas en el 
                         siglo XVI por el historiador indígena Fernando de Alva Ixtlilxochitl y traducido al español en el siglo
                          XIX por Faustino Chimalpopoca Galicia, de ahí su nombre también.    </dd>
                          <dd class="text-justify"> En éste documento se hace referencia al año de 10 Técpatl, correspondiente al año 1320 del calendario europeo 
                        y describe que “los chichimecas enviaron mensajeros a Coacalco, donde gobernaba Xóchimitl, para que hablaran con 
                        los tepanecas que hacía poco habían ido a asentarse en Coacalco.” </dd>
                        <dd class="text-justify"> No es el único códice en dónde aparece el topónimo de nuestro actual municipio; en la Matrícula de Tributos, 
                        documento fechado entre 1522 y 1530 de manufactura indígena, pero ya de la época virreinal, y en el cual se 
                        describen los artículos utilizados como tributos para la Triple Alianza,  aparece representado en la lámina 7 en 
                        la parte inferior derecha la casa de la serpiente.  </dd>
                        <dd class="text-justify"> Así mismo, en el Códice Mendozino, el cual está fechado en el año 1542 y que fue mandado a hacer por parte del 
                        virrey Antonio de Mendoza con el fin de tener un panorama político, económico y social de los territorios recién 
                        conquistados, aparece en la lámina 24,  en la parte inferior izquierda, el glifo  toponímico de Coacalco y el nombre 
                        escrito ya con la influencia hispánica. </dd>
                        <dd class="text-justify"> Por último y de suma importancia es el Códice Coacalco (Cohualcalco), el cual se encuentra actualmente en la Universidad
                         de Tulane, Estados Unidos de América, mismo que forma parte del grupo de códices Techialoyan escritos en el siglo XVI 
                         en lengua náhuatl.  </dd>
                    <dd class="text-justify"> Este documento hace una descripción detallada de “fundaciones de pueblos, batallas, inauguraciones, muertes, marcas de 
                        límites, entierros e información genealógica, efectuados todos en los siglos XIV y XV, están registrados en esta relación 
                        náhuatl, la cual continua después con una narración de la llegada de la fe católica y los bautismos a manos de fray Toribio 
                        de Benavente Motolinía y del conquistador Hernando Cortés (…) Además, el códice proporciona detalles de los descendientes de 
                        los gobernadores locales, a uno de los cuales, que sirvió supuestamente como juez, se le encomendó examinar los tlalámatl o 
                        documentos de tierras indígenas”, según describen los investigadores del Colegio Mexiquense, Xavier Noguez y Stephanie. Con 
                        referencia a la composición de la palabra Coacalco, se puede decir que tiene las raíces Coa, de coatl, serpiente; cal, de calli, 
                        casa; co, locativo, en, por lo que Coacalco se traduciría como “En la casa de la serpiente” o “Casa de la Serpiente”. </dd>
            <br>        <dt> <h3> <strong>	Encuentro de dos Mundos: La epoca Virreinal y su nueva espiritualidad  </strong> </h3></dt>
                <br>    <dd class="text-justify"> Ahora bien, como usted amable lector recordará, la conquista se dividió en dos partes, la primera de ellas que fue la militar, 
                        trajo consigo una serie de acontecimientos sangrientos que terminó con el dominio mexica sobre gran parte del territorio mesoamericano, 
                        tardándose aún muchos pueblos del septentrión en ser dominados por los ibéricos. La otra conquista, la espiritual, atraería una nueva fe,
                        con la cual los indígenas tuvieron que modificar el culto a sus antiguas deidades para enfocarse a un solo dios, el cual arribaría con una
                        serie de santas, santos y advocaciones enfocadas a una religión con preceptos y conceptos nuevos.  </dd>
                    <dd class="text-justify"> La labor evangelizadora que ejercieron los primeros misioneros franciscanos sobre estas tierras fue muy importante, pues en muchos casos 
                        trató de entender la visión de los antiguos pobladores que habitaban estos lugares, aunque también desafortunadamente en muchos otros casos, 
                        la fe nubló el entendimiento humanista europeo de la época y contribuyó a la destrucción de cientos de registros tanto edificados como en papel 
                        de las culturas precolombinas.  </dd>
                        <dd class="text-justify"> Además de estas dos conquistas, hay una tercera que llega a estas tierras de forma silenciosa pero cruel y mortal: las 
                            enfermedades que cruzaron el extenso océano Atlántico y desembarcaron cual jinete del apocalipsis, exterminando a millones de indígenas, cuyos 
                            hábitos higiénicos era muy diferentes a las insalubridades europeas, lo que constituyó un problema demográfico grave, a tal punto de mermar la 
                            población de casi 18 millones de indígenas a casi 3 millones en tan solo 50 años. El caso de Coacalco no fue la excepción, dada su cercanía con 
                            lo que se convertiría en la capital del virreinato de la Nueva España, la Muy Noble, Leal, Insigne e Imperial Ciudad de México, pues de los ocho 
                            barrios que constituían el territorio en la época prehispánica, se formaron congregaciones de indios en lo que ahora son nuestros tres pueblos 
                            originarios: San Francisco Coacalco, Santa María Magdalena Huizachitla, y San Lorenzo Tetlixtac, conservando su nombre prehispánico en simbiosis con el santoral gregoriano europeo. </dd>
                            <dd class="text-justify"> Las edificaciones religiosas virreinales dan fe de ello. El arte tequitqui en la de San Francisco adorna los plintos y capiteles de lo que fuera su capilla abierta, testimonio fehaciente de la evangelización del siglo XVI. Nuevos caminos cruzarían nuestro territorio: un Camino Real que iría Tierra Adentro, desviando uno de sus ramales hacia la casa de la serpiente, donde transitarían mercaderías, bestias de carga, alimentos, idioma, cultura antigua y recién llegada. Manos coacalquenses ayudarían a construir una de las mayores catedrales del hemisferio occidental: la Metropolitana de la Ciudad de México.  </dd> 
              <br>              <dt> <h3> <strong>	Campanas a pique por la libertad: El siglo XIX, aires de cambios.   </strong> </h3></dt>  
                  <br>          <dd class="text-justify"> El siglo XIX iniciaría con un grito de libertad, la emancipación de la América española convulsionaría todo un territorio que apenas entendía el significado de independencia. El cobijo imperial llegaría, al igual que su inminente fracaso; otras formas de entender el territorio, las leyes, costumbres, pensamientos se apropiarían de un tiempo decimonónico turbulento. Conservadores, liberales, federalismo, centralismo, república restaurada y al igual que Santa Anna, México perdería una extremidad, la mitad de su territorio sería mutilado, invadido y años después declarado en bancarrota.  </dd> 
                            <dd class="text-justify"> Bajo éste panorama, llegará un año clave para los coacalquenses: 1862, que dentro de todo ese panorama desolador, sería una luz que alimentaría el árbol de nuestra historia. Aquel doce de febrero, el Gobernador del Estado de México, General Felipe Benicio Berriozábal Basabe, alentado por su gran amigo, Don Manuel Alas Sardaneta, firmará el decreto que nos verá nacer como municipio. Dábamos un gran paso en nuestra historia ancestral, una flamante etapa en el devenir de orden y progreso.  </dd> 
                      <br>      <dt> <h3> <strong> De la modernidad a lo contemporáneo: El siglo XX, cuando la historia se acelera.   </strong> </h3></dt>
                          <br>  <dd class="text-justify"> Ranchos y haciendas prosperaron por toda la región, campos que vieron entrar el siglo XX con una atroz lucha armada, un nuevo orden constitucional, el nacimiento de nuestra actual democracia. También esa centuria llegaría con las tecnologías que caracterizaron el siglo pasado: las viejas carretas fueron reemplazadas por automóviles y otros medios de transporte cada vez más masivos; los caminos de herradura, apisonados por los pasos del tiempo, se asfaltaron y dieron pie a diferentes vialidades. La electrificación, servicios públicos.  El poblamiento del municipio fue en crecimiento, las unidades habitacionales, fraccionamientos, condominios, conjuntos urbanos hicieron su aparición, las colonias se expandieron, frescas caras sonreían día a día al ver un nuevo amanecer en Coacalco.  </dd>
                            <dd class="text-justify"> La urbanización del siglo XX trajo consigo nuevos pobladores que adoptaron este municipio como su hogar, así como lo hicieron migrantes de otras nacionalidades durante el siglo anterior; fraccionamientos, unidades habitacionales, condominios y colonias se sumaron a los pueblos ya establecidos, haciendo de Coacalco de Berriozábal en la actualidad uno de los municipios a nivel nacional cuya infraestructura  y servicios es de las más sobresalientes; así como su alto porcentaje de alfabetización y número de centros educativos. La actividad comercial es de las más importantes del norte del Valle de México, pues concentra un gran número de plazas que ofrecen servicios de entretenimiento y productos de primer nivel, fungiendo como puntos de reunión de las familias coacalquenses, propiciando la sana convivencia social.  </dd>
                     <br>       <dt> <h3> <strong> El Siglo XXI: Coacalco hoy, el fuego nuevo en el siglo nuevo milenio.   </strong> </h3></dt>
                      <br>      <dd class="text-justify"> La magia y el encanto se han negado a desaparecer y han aprendido a coexistir entre el ajetreo natural de una ciudad con la tranquilidad que da caminar calles pueblerinas, saludando por su nombre a los vecinos, surtiendo la despensa diaria en los locales de costumbre, respirando el frescor del sereno matutino viendo una gama de tonalidades verdosas y azules que ofrece la milenaria serranía y combinando sonidos urbanos con el canto de pájaros, el repique de campanas y el murmullo de sus habitantes.  </dd>
                            <dd class="text-justify"> El árbol de la memoria viva alcanzaría con sus ramas el siglo XXI, nuestra centuria, en una ciudad que se nutre con las raíces de quienes dejaron su huella aquí: hombres, mujeres, niños, adultos mayores. Coacalquenses que hoy en día se sienten orgullosos de seguir el legado de sus ancestros, creando un sentido de pertenencia e identidad a 159 años del nacimiento de nuestro municipio, con una historia ancestral </dd>
